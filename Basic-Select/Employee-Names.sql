/* DESCRIPTION: Write a query that prints a list of employee names 
                (i.e.: the name attribute) from the Employee table
                in alphabetical order. */
-- AUTHOR:      https://github.com/theSwapnilSaste/
-- DATE:        25-JAN-2020
-- ORACLE
SELECT      name
FROM        employee
ORDER BY    name;